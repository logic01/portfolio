
namespace LeetCode.Wave.Two.Util
{
    public class TreeNode
    {
        public int Value;
        public TreeNode? Left;
        public TreeNode? Right;

        public TreeNode(int value = 0, TreeNode? left = null, TreeNode? right = null)
        {
            this.Value = value;
            this.Left = left;
            this.Right = right;
        }
    }
}
